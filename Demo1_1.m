% 周期方波信号
clear;  % 清空工作区变量

td = 1 / 100000;         % 采样周期
whos td   % 查看变量类型和大小
disp(td)  % 显示 td 的值
t = 0:td:1;              % 时间向量（1秒）

% 生成不同占空比的方波信号
x1 = square(2*pi*40*t, 25);   % 占空比 25%
x2 = square(2*pi*40*t, 50);   % 占空比 50%
x3 = square(2*pi*40*t, 75);   % 占空比 75%

% 绘图部分
subplot(3,1,1);           % 设置 3 行 1 列子图，绘制第 1 个图
plot(t, x1);
title('占空比 25%');
axis([0 0.2 -1.5 1.5]);   % 设置坐标轴范围

subplot(3,1,2);           % 第 2 个子图
plot(t, x2);
title('占空比 50%');
axis([0 0.2 -1.5 1.5]);

subplot(3,1,3);           % 第 3 个子图
plot(t, x3);
title('占空比 75%');
axis([0 0.2 -1.5 1.5]);
